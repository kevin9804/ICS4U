// The "Pet" class.
public class Pet
{
    private String name;
    private String animal;
    private int age;

    public void setName (String nam)
    {
	name = nam;
    }


    public void setAnimal (String ani)
    {
	animal = ani;
    }


    public void setAge (int a)
    {
	age = a;
    }


    public String getName ()
    {
	return name;
    }


    public String getAnimal ()
    {
	return animal;
    }


    public int getAge ()
    {
	return age;
    }
}
